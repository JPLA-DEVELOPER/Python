#----------------------TUPLAS --------------------------
#---CARACTERÍSTICAS -----------
#--Ordenados
#--Permite valores duplicados
#--São indexados
#--Permite dados heterogêneos
#--Elementos não-intercambiados(não dá para trocar elementos de lugar dentro de uma tupla)


#--------------------------CRIAÇÃO------------------------------------
tupla = (1, 2, 3)
print(f'Tupla: {tupla}')

#outra forma de criar tuplas:

lista = [1,2,3]
tupla2 = tuple(lista)
print(f'Tupla2: {tupla2}')

