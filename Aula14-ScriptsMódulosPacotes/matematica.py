def soma(numero_1, numero_2):
    print(f'Foi chamada a função soma() com os argumentos {numero_1} e {numero_2}')
    return numero_1 + numero_2


def subtração(numero_1, numero_2):
    print(f'Foi chamada a função subtração() com os argumentos {numero_1} e {numero_2}')
    return numero_1 - numero_2